"""Helpers to lay out a YOLO-format dataset (images/{train,val}, labels/{train,val})."""

from __future__ import annotations

import random
import shutil
from pathlib import Path
from typing import Dict, List, Literal, Optional, Sequence, Tuple

from utils.logger import get_logger

logger = get_logger(__name__)

ImagePair = Tuple[Path, Path]  # (image_path, label_path)
TransferMode = Literal["copy", "symlink", "move"]

IMAGE_EXTENSIONS = {".jpg", ".jpeg", ".png", ".bmp", ".tif", ".tiff", ".webp"}


def discover_pairs(
    images_dir: Path,
    labels_dir: Path,
    *,
    require_labels: bool = True,
) -> List[ImagePair]:
    """Match every image in ``images_dir`` to a ``.txt`` label in ``labels_dir`` by stem.

    Args:
        images_dir: Folder containing the raw images.
        labels_dir: Folder containing YOLO ``.txt`` annotation files.
        require_labels: If True, raise when an image has no matching label.
            If False, images without labels are skipped with a warning.

    Returns:
        List of ``(image_path, label_path)`` tuples sorted by image path.
    """
    images_dir = Path(images_dir)
    labels_dir = Path(labels_dir)

    if not images_dir.is_dir():
        raise FileNotFoundError(f"Images directory not found: {images_dir}")
    if not labels_dir.is_dir():
        raise FileNotFoundError(f"Labels directory not found: {labels_dir}")

    images = sorted(
        p for p in images_dir.iterdir()
        if p.is_file() and p.suffix.lower() in IMAGE_EXTENSIONS
    )

    pairs: List[ImagePair] = []
    missing: List[Path] = []
    for img in images:
        label = labels_dir / f"{img.stem}.txt"
        if label.exists():
            pairs.append((img, label))
        else:
            missing.append(img)

    if missing:
        msg = f"{len(missing)} image(s) have no matching label (e.g. {missing[0].name})"
        if require_labels:
            raise FileNotFoundError(msg)
        logger.warning("%s — skipping", msg)

    if not pairs:
        raise ValueError(f"No (image, label) pairs found under {images_dir} / {labels_dir}")

    logger.info("Discovered %d valid (image, label) pairs in %s", len(pairs), images_dir)
    return pairs


def split_pairs(
    pairs: Sequence[ImagePair],
    val_split: float = 0.2,
    *,
    seed: int = 42,
) -> Tuple[List[ImagePair], List[ImagePair]]:
    """Deterministically shuffle and split pairs into (train, val) lists."""
    if not 0.0 < val_split < 1.0:
        raise ValueError(f"val_split must be in (0, 1), got {val_split}")

    rng = random.Random(seed)
    shuffled = list(pairs)
    rng.shuffle(shuffled)

    val_size = max(1, round(len(shuffled) * val_split))
    val_size = min(val_size, len(shuffled) - 1)  # always keep at least 1 in train

    val = shuffled[:val_size]
    train = shuffled[val_size:]
    logger.info(
        "Split %d pairs into train=%d, val=%d (val_split=%.2f, seed=%d)",
        len(shuffled), len(train), len(val), val_split, seed,
    )
    return train, val


def _transfer(src: Path, dst: Path, mode: TransferMode) -> None:
    dst.parent.mkdir(parents=True, exist_ok=True)
    if dst.exists() or dst.is_symlink():
        dst.unlink()

    if mode == "copy":
        shutil.copy2(src, dst)
    elif mode == "move":
        shutil.move(str(src), str(dst))
    elif mode == "symlink":
        dst.symlink_to(src.resolve())
    else:  # pragma: no cover
        raise ValueError(f"Unknown transfer mode: {mode}")


def write_split(
    pairs: Sequence[ImagePair],
    output_dir: Path,
    split_name: str,
    *,
    mode: TransferMode = "copy",
) -> None:
    """Place ``pairs`` under ``output_dir/{images,labels}/<split_name>``."""
    images_out = output_dir / "images" / split_name
    labels_out = output_dir / "labels" / split_name
    images_out.mkdir(parents=True, exist_ok=True)
    labels_out.mkdir(parents=True, exist_ok=True)

    for img, lbl in pairs:
        _transfer(img, images_out / img.name, mode)
        _transfer(lbl, labels_out / lbl.name, mode)

    logger.info("Wrote %d pairs into %s (mode=%s)", len(pairs), images_out, mode)


def write_dataset_yaml(
    output_dir: Path,
    class_names: Sequence[str],
    *,
    yaml_path: Optional[Path] = None,
    use_absolute_path: bool = True,
) -> Path:
    """Write a YOLO-compatible dataset YAML next to the dataset root."""
    output_dir = Path(output_dir)
    yaml_path = Path(yaml_path) if yaml_path else output_dir / "dataset.yaml"

    root = str(output_dir.resolve()) if use_absolute_path else "."
    names_block = "\n".join(f"  {i}: {name}" for i, name in enumerate(class_names))

    contents = (
        "# Auto-generated by utils.dataset.write_dataset_yaml\n"
        f"path: {root}\n"
        "train: images/train\n"
        "val: images/val\n"
        "\n"
        "names:\n"
        f"{names_block}\n"
    )
    yaml_path.parent.mkdir(parents=True, exist_ok=True)
    yaml_path.write_text(contents, encoding="utf-8")
    logger.info("Wrote dataset YAML to %s", yaml_path)
    return yaml_path


def prepare_dataset(
    images_dir: Path,
    labels_dir: Path,
    output_dir: Path,
    class_names: Sequence[str],
    *,
    val_split: float = 0.2,
    seed: int = 42,
    mode: TransferMode = "copy",
    require_labels: bool = True,
    write_yaml: bool = True,
) -> Dict[str, object]:
    """One-call orchestrator: discover pairs, split them, lay them out and write the YAML.

    Returns a small summary dict with counts and paths.
    """
    pairs = discover_pairs(images_dir, labels_dir, require_labels=require_labels)
    train, val = split_pairs(pairs, val_split=val_split, seed=seed)

    write_split(train, output_dir, "train", mode=mode)
    write_split(val, output_dir, "val", mode=mode)

    yaml_path: Optional[Path] = None
    if write_yaml:
        yaml_path = write_dataset_yaml(output_dir, class_names)

    summary: Dict[str, object] = {
        "output_dir": str(Path(output_dir).resolve()),
        "yaml_path": str(yaml_path) if yaml_path else None,
        "classes": list(class_names),
        "counts": {"train": len(train), "val": len(val), "total": len(pairs)},
        "mode": mode,
    }
    return summary
